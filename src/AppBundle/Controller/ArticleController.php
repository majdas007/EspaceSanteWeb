       // $article->setDatePub($now);
       // $article->setIdCat(3);
       /// $article->setIdUser(2);

        $form = $this->createForm('AppBundle\Form\ArticleType', $article);
        $form->handleRequest($request);

        if ($form->isSubmitted() && $form->isValid()) {

          /*  $em = $this->getDoctrine()->getManager();
